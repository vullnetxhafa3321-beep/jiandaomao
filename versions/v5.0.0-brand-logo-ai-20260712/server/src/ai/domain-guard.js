/**
 * 检查用户输入是否属于宠物救助领域
 * @param {string} input - 用户最新一条消息
 * @returns {{ allowed: boolean, reply?: string }}
 */
export function domainGuard(input) {
  const q = input.toLowerCase().trim();

  // 空输入直接放行（让 AI 处理）
  if (!q) return { allowed: true };

  // 黑名单检测：命中明确无关词
  const hitBlacklist = OFF_TOPIC_BLACKLIST.some(kw => q.includes(kw.toLowerCase()));
  if (hitBlacklist) return REFUSAL_REPLY;

  // 白名单检测：至少命中一个宠物相关词
  const hitWhitelist = PET_WHITELIST.some(kw => q.includes(kw.toLowerCase()));
  if (hitWhitelist) return { allowed: true };

  // 都未命中：对于短输入（可能不包含明确关键词但仍是宠物相关），放行让 AI 判断
  // 对于长输入且无任何宠物词，拒绝
  if (q.length > 30) {
    return REFUSAL_REPLY;
  }

  // 短输入放行（如"你好""谢谢""怎么办"等简短对话）
  return { allowed: true };
}

/**
 * 批量检查对话历史（检查最后一条用户消息）
 * @param {Array<{role: string, content: string}>} messages
 * @returns {{ allowed: boolean, reply?: string }}
 */
export function checkMessages(messages) {
  if (!messages || messages.length === 0) return { allowed: true };

  // 取最后一条用户消息
  const userMsgs = messages.filter(m => m.role === 'user');
  if (userMsgs.length === 0) return { allowed: true };

  const lastUserMsg = userMsgs[userMsgs.length - 1].content;
  return domainGuard(lastUserMsg);
}
